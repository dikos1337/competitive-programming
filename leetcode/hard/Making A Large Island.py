def print_grid(grid):
    for x in grid:
        print(x)


class Solution:
    def compute_island_size(
        self, grid: List[List[int]], n, pos_y, pos_x, visited=set()
    ) -> int:
        pos_to_check = []

        visited |= {(pos_y, pos_x)}  # add self

        # check down
        if pos_y < n - 1:
            if grid[pos_y + 1][pos_x] == 1:
                pos_to_check += [(pos_y + 1, pos_x)]

        # check left
        if pos_x > 0:
            if grid[pos_y][pos_x - 1] == 1:
                pos_to_check += [(pos_y, pos_x - 1)]

        # check up
        if pos_y > 0:
            if grid[pos_y - 1][pos_x] == 1:
                pos_to_check += [(pos_y - 1, pos_x)]

        # check right
        if pos_x < n - 1:
            if grid[pos_y][pos_x + 1] == 1:
                pos_to_check += [(pos_y, pos_x + 1)]

        for pos_y, pos_x in pos_to_check:
            if (pos_y, pos_x) not in visited:
                visited |= {(pos_y, pos_x)}

                visited_future = self.compute_island_size(
                    grid, n, pos_y, pos_x, visited
                )
                visited |= visited_future

        return visited

    def largestIsland(self, grid: List[List[int]]) -> int:
        if grid == [[1]]:
            return 1
        n = len(grid)
        ones = sum([y.count(1) for y in grid])

        # print(f"{ones = }", f"{n = }")

        if ones == n * n:
            return ones
        elif ones == (n * n) - 1:
            return ones + 1

        # self.global_cache = set()
        max_island_size = 0
        for y in range(n):
            for x in range(n):
                if grid[y][x] == 0:
                    grid[y][x] = 1
                    # print("-" * 10)
                    # print(f"checking {y = } {x = }")
                    # print_grid(grid)
                    island_size = self.compute_island_size(grid, n, y, x, visited=set())
                    # print(f"{len(island_size)},{island_size = }")
                    # print("-" * 10)
                    len_island_size = len(island_size)
                    if len_island_size > max_island_size:
                        max_island_size = len_island_size

                    grid[y][x] = 0

        # print(f"{max_island_size = }")

        return max_island_size


